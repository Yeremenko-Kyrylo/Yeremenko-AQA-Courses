
// arifmetics.js

// Змінні числових типів
let a = 10;
let b = 3;

// Арифметичні операції над числами
console.log("Додавання:", a + b);
console.log("Віднімання:", a - b);
console.log("Множення:", a * b);
console.log("Ділення:", a / b);
console.log("Залишок від ділення:", a % b);

// Змінні різних типів
let str = "5";
let num = 2;

// Операції над різними типами
console.log("Конкатенація:", str + num);
console.log("Неявне перетворення (додавання):", +str + num);
console.log("Неявне перетворення (множення):", str * num);
