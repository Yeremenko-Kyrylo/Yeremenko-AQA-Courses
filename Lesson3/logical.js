
// logical.js

let x = 5;
let y = "5";
let z = true;

// Операції порівняння
console.log("x == y:", x == y);
console.log("x === y:", x === y);
console.log("x != y:", x != y);
console.log("x !== y:", x !== y);
console.log("x > 3:", x > 3);
console.log("x < 10:", x < 10);

// Логічні операції
console.log("z && (x > 3):", z && (x > 3));
console.log("z || (x < 3):", z || (x < 3));
console.log("!z:", !z);
